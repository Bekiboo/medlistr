  <section class="mt-8">
    <h2 class="text-2xl">Why Now?</h2>
    <p>
      We have been working since 2012 on healthcare price transparency tools.
      <br />
      The industry is still far less transparent than it needs to be, but it has
      also come a long way. Years ago many healthcare providers made it very difficult
      to get price information up front. But today, some of these same companies
      who made it difficult to learn their prices are not voluntarily posting prices
      on their websites.
      <br />
      In addition, healthcare providers who have been less transparent are now facing
      legal pressure. Both the Trump and Biden administration have supported greater
      price transparency in healthcare. The laws they have created were met with
      opposition at first but have since been upheld in court. These laws are the
      first to require medical facilities to release price information for select
      services.
      <br />
      In short, providers have become far more transparent (either voluntarily or
      by force) and they are sharing their price information online. And we intend
      to take it at least one step further.
    </p>
  </section>

